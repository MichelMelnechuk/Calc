#import "UIColor+Branding.h"

@implementation UIColor (Branding)

+ (UIColor *)brandGreenColor
{
    return [UIColor colorWithRed:36.0f / 255.f
                           green:170.f / 255.f
                            blue:22.f /255.f
                           alpha:1.f];
}

+(UIColor*)brandYellowColor
{
    return [UIColor colorWithRed:250.0f / 255.f
                           green:246.f / 255.f
                            blue:181.f /255.f
                           alpha:1.f];
}

@end
